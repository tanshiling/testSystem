package com.tsl.model;

public class ShortAnswer {

    private int said;
    private int kno;
    private String sacontent;
    private String sabasis;
    private String saanswer;
    private int salevel;

    public int getSaid() {
        return said;
    }

    public void setSaid(int said) {
        this.said = said;
    }

    public int getKno() {
        return kno;
    }

    public void setKno(int kno) {
        this.kno = kno;
    }

    public String getSacontent() {
        return sacontent;
    }

    public void setSacontent(String sacontent) {
        this.sacontent = sacontent;
    }

    public String getSabasis() {
        return sabasis;
    }

    public void setSabasis(String sabasis) {
        this.sabasis = sabasis;
    }

    public String getSaanswer() {
        return saanswer;
    }

    public void setSaanswer(String saanswer) {
        this.saanswer = saanswer;
    }

    public int getSalevel() {
        return salevel;
    }

    public void setSalevel(int salevel) {
        this.salevel = salevel;
    }
}
