package com.tsl.model;

public class Code {

    private int cid;
    private int kno;
    private String ccontent;
    private String cbasis;
    private String canswer;
    private String canswerp;
    private int clevel;

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public int getKno() {
        return kno;
    }

    public void setKno(int kno) {
        this.kno = kno;
    }

    public String getCcontent() {
        return ccontent;
    }

    public void setCcontent(String ccontent) {
        this.ccontent = ccontent;
    }

    public String getCbasis() {
        return cbasis;
    }

    public void setCbasis(String cbasis) {
        this.cbasis = cbasis;
    }

    public String getCanswer() {
        return canswer;
    }

    public void setCanswer(String canswer) {
        this.canswer = canswer;
    }

    public String getCanswerp() {
        return canswerp;
    }

    public void setCanswerp(String canswerp) {
        this.canswerp = canswerp;
    }

    public int getClevel() {
        return clevel;
    }

    public void setClevel(int clevel) {
        this.clevel = clevel;
    }
}
